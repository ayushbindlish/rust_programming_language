pub fn numeric_operation() {

    // addition
    let sum = 5 + 10;
    println!("sum is {}",sum);

    // subtraction
    let difference = 95.589 - 4.36;
    println!("difference is {}",difference);

    // multiplication
    let product = 4 * 30;
    println!("product is {}",product);

    // division
    let quotient = 56.7 / 32.2;
    println!("quotient is {}",quotient);

    // remainder
    let remainder = 43 % 5;
    println!("remainder is {}",remainder);
}